    private void mostrar(javax.swing.JTable jt, String sql) {
        try {
            EmpleadoBeans cb = new EmpleadoBeans();
            ResultSet rs;
            DefaultTableModel modelo = new DefaultTableModel();
            jt.setModel(modelo);
            rs = cb.consultaTabla(sql);
            ResultSetMetaData rsMd;
            rsMd = rs.getMetaData();
            int cantcolumnas = rsMd.getColumnCount();
            for (int i = 1; i <= cantcolumnas; i++) {
                modelo.addColumn(rsMd.getColumnLabel(i));
            }
            while (rs.next()) {
                Object[] columna = new Object[cantcolumnas];
                for (int i = 0; i < cantcolumnas; i++) {
                    columna[i] = rs.getObject(i + 1);
                }
                modelo.addRow(columna);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error" + e.toString());
        }
    }

    private void MouseClick() {
        int fila = jtEmpleado.getSelectedRow();
        cbid.setSelectedItem(jtEmpleado.getModel().getValueAt(fila, 0).toString());
        cbidcargo.setSelectedItem(jtEmpleado.getModel().getValueAt(fila, 1).toString());
        cbidhorario.setSelectedItem(jtEmpleado.getModel().getValueAt(fila, 2).toString());
        txtcedularuc.setText(jtEmpleado.getModel().getValueAt(fila, 3).toString());
        txtnombres.setText(jtEmpleado.getModel().getValueAt(fila, 4).toString());
        txttelefono.setText(jtEmpleado.getModel().getValueAt(fila, 5).toString());
        txtdireccion.setText(jtEmpleado.getModel().getValueAt(fila, 6).toString());
    }

    public void Actualizar() {
        try {
            EmpleadoBeans cb = new EmpleadoBeans();
            cb.setId_Empleado(Integer.parseInt(cbid.getSelectedItem().toString()));
            cb.setId_cargo(Integer.parseInt(cbidcargo.getSelectedItem().toString()));
            cb.setId_horario(Integer.parseInt(cbidhorario.getSelectedItem().toString()));
            cb.setCedula(txtcedularuc.getText());
            cb.setNombres(txtnombres.getText());
            cb.setTelefono(txttelefono.getText());
            cb.setDireccion(txtdireccion.getText());
            cb.Actualizar_Empleado();
            this.mostrar(jtEmpleado, "SELECT * FROM empleado WHERE id_empleado = " + cb.getId_Empleado());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al actualizar el empleado: " + e.toString());
        }
    }

    public void Eliminar() {
        try {
            EmpleadoBeans cb = new EmpleadoBeans();
            cb.setId_Empleado(Integer.parseInt(cbid.getSelectedItem().toString()));
            cb.Eliminar_Empleado();
            this.mostrar(jtEmpleado, "SELECT * FROM empleado WHERE id_empleado = " + cb.getId_Empleado());
        } catch (Exception e) {
        }
    }

