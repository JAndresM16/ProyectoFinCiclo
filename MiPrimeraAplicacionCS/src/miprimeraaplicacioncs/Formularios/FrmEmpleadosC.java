    public void cargarComboIdEmpleados() {
        try {
            ResultSet rs = empleadoBeans.consultaTabla("SELECT id_empleado FROM empleado");
            while (rs.next()) {
                cbid.addItem(rs.getString("id_empleado"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al cargar las IDs de empleados: " + e.toString());
        }
    }

    public void mostrarDatosEmpleadoSeleccionado() {
        try {
            String empleadoId = cbid.getSelectedItem().toString();
            ResultSet rs = empleadoBeans.consultaTabla("SELECT * FROM empleado WHERE id_empleado = " + empleadoId);
            DefaultTableModel modelo = (DefaultTableModel) jtEmpleado.getModel();
            modelo.setRowCount(0);
            while (rs.next()) {
                Object[] fila = {rs.getInt("id_empleado"), rs.getInt("id_cargo"), rs.getInt("id_horario"), rs.getString("cedula"), rs.getString("nombres"), rs.getString("telefono"), rs.getString("direccion")};
                modelo.addRow(fila);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al obtener los datos del empleado: " + e.toString());
        }
    }

